extern const std::vector<std::string> kIncludeDirectories;
// const std::string kTestSource = "#include <stdio.h>\n#include <curl/curl.h>";
const std::string kTestSource = "class Test { };";

int main(int argc, char *argv[], char *envp[]) {
  static_cast<void>(argc);
  static_cast<void>(argv);
  static_cast<void>(envp);

  std::unique_ptr<trailofbits::ISourceCodeParser> parser;
  auto status =
      trailofbits::CreateSourceCodeParser(parser, kIncludeDirectories);
  if (!status.succeeded()) {
    std::cerr << status.toString() << "\n";
    return 1;
  }

  status = parser->processBuffer(kTestSource);
  if (!status.succeeded()) {
    std::cerr << status.toString() << "\n";
    return 1;
  }

  if (status.statusCode() ==
      trailofbits::ISourceCodeParser::StatusCode::SucceededWithWarnings) {
    std::cout << "Warnings: " << status.message() << "\n";
  }

  return 0;
}

